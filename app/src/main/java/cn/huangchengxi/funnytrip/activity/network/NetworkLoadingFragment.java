package cn.huangchengxi.funnytrip.activity.network;

import android.content.Context;
import android.net.Uri;
import android.os.Bundle;

import androidx.fragment.app.Fragment;

import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ImageView;

import com.bumptech.glide.Glide;

import cn.huangchengxi.funnytrip.R;

public class NetworkLoadingFragment extends Fragment {
    private OnFragmentInteractionListener mListener;
    private ImageView imageView;

    public NetworkLoadingFragment() {
        // Required empty public constructor
    }

    public static NetworkLoadingFragment newInstance() {
        NetworkLoadingFragment fragment = new NetworkLoadingFragment();
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        View view= inflater.inflate(R.layout.fragment_network_loading, container, false);
        imageView=view.findViewById(R.id.loading_img);
        Glide.with(getContext()).load(R.drawable.network_loading).into(imageView);
        return view;
    }

    // TODO: Rename method, update argument and hook method into UI event
    public void onButtonPressed(Uri uri) {
        if (mListener != null) {
            mListener.onFragmentInteraction(uri);
        }
    }

    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        if (context instanceof OnFragmentInteractionListener) {
            mListener = (OnFragmentInteractionListener) context;
        } else {
        }
    }

    @Override
    public void onDetach() {
        super.onDetach();
        mListener = null;
    }
    public interface OnFragmentInteractionListener {
        void onFragmentInteraction(Uri uri);
    }
}
